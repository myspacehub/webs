function getSearchResults() {
  const groups = [];
  COURSE.forEach((subject, subjectIndex) => {
    subject.volumes.forEach((volume, volumeIndex) => {
      const matchedUnits = volume.units
        .map((unit, unitIndex) => ({ unit, unitIndex }))
        .filter(({ unit }) => containsQuery(subject.name, volume.name, unit));

      if (matchedUnits.length) {
        groups.push({ subject, subjectIndex, volume, volumeIndex, matchedUnits });
      }
    });
  });
  return groups;
}

