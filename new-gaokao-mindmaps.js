  function subjectKeyOf(name) {
    const text = String(name || "");
    if (text.includes("数学")) return "数学";
    if (text.includes("英语")) return "英语";
    if (text.includes("物理")) return "物理";
    if (text.includes("化学")) return "化学";
    if (text.includes("生物")) return "生物";
    if (text.includes("历史")) return "历史";
    if (text.includes("地理")) return "地理";
    if (text.includes("政治") || text.includes("思想政治")) return "思想政治";
    return "语文";
  }

  function normalize(text) {
    return String(text || "")
      .toLowerCase()
      .replace(/[（(].*?[）)]/g, "")
      .replace(/[第章节课单元\s·、，,。:：；;《》“”"'\-—–_]/g, "");
  }

  function getSubject(subjectName) {
    const key = subjectKeyOf(subjectName);
    return NEW_GAOKAO_MINDMAPS.subjects.find((subject) => subject.key === key) || null;
  }

  function findNodeByPath(nodes, path) {
    let currentNodes = nodes;
    let current = null;
    for (const title of path || []) {
      current = currentNodes.find((item) => item.title === title);
      if (!current) return null;
      currentNodes = current.children || [];
    }
    return current;
  }

  function walkNodes(nodes, visitor, path = []) {
    for (const item of nodes || []) {
      const nextPath = [...path, item.title];
      visitor(item, nextPath);
      walkNodes(item.children || [], visitor, nextPath);
    }
  }

  function findUnitNode(subject, volumeName, unitName) {
    if (!subject) return null;
    const directPath = subject.unitPaths?.[`${volumeName}|${unitName}`] || subject.unitPaths?.[unitName];
    if (directPath) return findNodeByPath(subject.roots, directPath);

    const unitNorm = normalize(unitName);
    const volumeNorm = normalize(volumeName);
    const candidates = [];
    walkNodes(subject.roots, (item, path) => {
      const titleNorm = normalize(item.title);
      if (!titleNorm) return;
      let score = 0;
      if (unitNorm === titleNorm) score += 1000;
      if (unitNorm.includes(titleNorm) || titleNorm.includes(unitNorm)) score += Math.min(500, titleNorm.length * 16);
      if (volumeNorm && normalize(path[0] || "").includes(volumeNorm)) score += 40;
      if (/^(unit\d+|[一二三四五六七八九十\d]+章)/.test(titleNorm)) score += 20;
      if (score > 0) candidates.push({ item, score, depth: path.length });
    });
    return candidates.sort((a, b) => b.score - a.score || b.depth - a.depth)[0]?.item || null;
  }

  function flattenLeaves(item, prefix = "") {
    if (typeof item === "string") return [`${prefix}${item}`];
    const title = item.title || "";
    const nextPrefix = prefix ? `${prefix} / ${title}：` : "";
    if (!item.children?.length) return title ? [prefix ? `${prefix}${title}` : title] : [];
    return item.children.flatMap((child) => {
      if (typeof child === "string") return [`${nextPrefix}${child}`];
      return flattenLeaves(child, prefix ? `${prefix} / ${title}` : title);
    });
  }

  function classify(points, marker) {
    return points.filter((point) => point.includes(marker));
  }

  function uniq(list) {
    return [...new Set(list)].filter(Boolean);
  }

  function branch(title, points) {
    return { title, points: uniq(points) };
  }

  function buildCuratedBranches(subjectName, volumeName, unitName) {
    const subject = getSubject(subjectName);
    const item = findUnitNode(subject, volumeName, unitName);
    if (!subject || !item) return null;

    const all = uniq(flattenLeaves(item));
    const core = classify(all, "🔹");
    const important = classify(all, "📌");
    const pitfalls = classify(all, "⚠️");
    const other = all.filter((point) => !/[🔹📌⚠️]/.test(point));

    return [
      branch("新高考导图定位", [
        `${subject.title} · ${subject.textbook}`,
        `当前节点：${volumeName} · ${unitName}`,
        `本节点已按“🔹核心概念、📌重点结论、⚠️高频易错点”整合，可直接复制到 XMind / ProcessOn / 幕布继续展开。`,
      ]),
      branch("🔹核心概念", core.length ? core : all.slice(0, 8)),
      branch("📌重点结论 / 方法模型", important.length ? important : all.slice(0, 8)),
      branch("⚠️高频易错点", pitfalls.length ? pitfalls : [`围绕「${unitName}」检查概念边界、适用条件、步骤顺序和材料限定词。`]),
      branch("全量导图节点", [...other, ...all]),
    ];
  }

  function treeTextNode(item, depth = 0) {
    const indent = depth === 0 ? "" : `${"  ".repeat(depth - 1)}${depth === 1 ? "├─ " : "│  ".repeat(Math.max(0, depth - 2)) + "├─ "}`;
    if (typeof item === "string") return `${indent}${item}`;
    const lines = [`${indent}${item.title}`];
    for (const child of item.children || []) lines.push(treeTextNode(child, depth + 1));
    return lines.join("\n");
  }

  window.NEW_GAOKAO_MINDMAPS = NEW_GAOKAO_MINDMAPS;
  window.getNewGaokaoSubjectMindMap = getSubject;
  window.createCuratedMindMapBranches = buildCuratedBranches;
  window.countNewGaokaoSubjectNodes = function countNewGaokaoSubjectNodes(subjectName) {
    const subject = getSubject(subjectName);
    if (!subject) return 0;
    let count = 1;
    walkNodes(subject.roots, (item) => {
      count += 1 + (item.children || []).filter((child) => typeof child === "string").length;
    });
    return count;
  };
  window.newGaokaoMindMapText = function newGaokaoMindMapText(subjectName) {
    const subject = getSubject(subjectName);
    if (!subject) return "";
    return [subject.title, `教材版本：${subject.textbook}`, NEW_GAOKAO_MINDMAPS.note, ...subject.roots.map((item) => treeTextNode(item, 1))].join("\n");
  };
